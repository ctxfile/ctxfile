import { existsSync, mkdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

/**
 * The Behavior Layer (design doc 11): one canonical behavior spec, rendered
 * into per-harness formats. MCP gives an agent tools; this gives it behavior:
 * checkpoint at the right moments, announce every save, detect handoffs. The
 * "software" is prose; this module is the renderer, the installer, and the
 * consent/pause state the server's guardrails read.
 */

export const BEHAVIOR_HARNESSES = [
  "claude-code",
  "cursor",
  "opencode",
  "gemini",
  "agents-md",
  "codex",
  "openclaw",
  "hermes",
  "generic",
] as const;
export type BehaviorHarness = (typeof BEHAVIOR_HARNESSES)[number];

/** The Agent Skills SKILL.md frontmatter + body, shared by every harness that
    reads a SKILL.md (Claude Code, OpenCode, OpenClaw, Hermes). One canonical
    body, one frontmatter schema (name + description), so the same skill is
    portable across their skill systems. */
function skillFile(body: string): string {
  return `---
name: ctxfile
description: Automatic context checkpointing via the ctxfile MCP tools. Use throughout every session - at session start (load context), when tasks complete or decisions are made (checkpoint), and when the user hands work off or says goodbye.
---

${body}
`;
}

/** Managed-block markers for append-style targets (AGENTS.md, .cursorrules),
    same pattern as the git hook: reinstall updates in place, uninstall is a
    clean cut. */
export const BEHAVIOR_BLOCK_BEGIN = "<!-- ctxfile:behaviors:begin (managed; do not edit inside) -->";
export const BEHAVIOR_BLOCK_END = "<!-- ctxfile:behaviors:end -->";

/** The canonical spec ships inside the npm package beside dist/. */
export function loadCanonicalBehaviors(): string {
  const here = path.dirname(fileURLToPath(import.meta.url));
  for (const candidate of [
    path.join(here, "..", "behaviors", "canonical.md"),
    path.join(here, "..", "..", "behaviors", "canonical.md"),
  ]) {
    if (existsSync(candidate)) return readFileSync(candidate, "utf8");
  }
  throw new Error("behaviors/canonical.md not found beside the ctxfile package");
}

export interface RenderedBehavior {
  harness: BehaviorHarness;
  /** Path relative to the render root (or the suggested install location). */
  relativePath: string;
  content: string;
}

/** Deterministic renders from the single source; the repo's committed
    render/ folder is regenerated by scripts/render-behaviors.mjs and a test
    fails if it drifts from this function's output. */
export function renderBehavior(harness: BehaviorHarness, canonical = loadCanonicalBehaviors()): RenderedBehavior {
  const body = canonical.trim();
  switch (harness) {
    case "claude-code":
      return { harness, relativePath: "claude-code/SKILL.md", content: skillFile(body) };
    case "opencode":
      // OpenCode also auto-reads .claude/skills/, but its own path is explicit.
      return { harness, relativePath: "opencode/SKILL.md", content: skillFile(body) };
    case "openclaw":
      return { harness, relativePath: "openclaw/SKILL.md", content: skillFile(body) };
    case "hermes":
      return { harness, relativePath: "hermes/SKILL.md", content: skillFile(body) };
    case "gemini":
      return {
        harness,
        relativePath: "gemini/GEMINI.md",
        content: `${BEHAVIOR_BLOCK_BEGIN}
## ctxfile: automatic context checkpointing

${body}
${BEHAVIOR_BLOCK_END}
`,
      };
    case "cursor":
      return {
        harness,
        relativePath: "cursor/ctxfile.mdc",
        content: `---
description: ctxfile automatic context checkpointing (announced, never silent)
alwaysApply: true
---

${body}
`,
      };
    case "agents-md":
      return {
        harness,
        relativePath: "agents-md/SECTION.md",
        content: `${BEHAVIOR_BLOCK_BEGIN}
## ctxfile: automatic context checkpointing

${body}
${BEHAVIOR_BLOCK_END}
`,
      };
    case "codex":
      return {
        harness,
        relativePath: "codex/instructions.md",
        content: `# ctxfile behaviors for Codex\n\nAdd this to your Codex instructions (global \`~/.codex/AGENTS.md\` or per-project AGENTS.md).\n\n${body}\n`,
      };
    case "generic":
      return {
        harness,
        relativePath: "generic/system-prompt.md",
        content: `# ctxfile behaviors (generic system-prompt block)\n\nPaste into the system prompt of any harness with the ctxfile MCP connected (OpenRouter wrappers, open models, custom agents).\n\n${body}\n`,
      };
  }
}

export function renderAllBehaviors(canonical = loadCanonicalBehaviors()): RenderedBehavior[] {
  return BEHAVIOR_HARNESSES.map((h) => renderBehavior(h, canonical));
}

